#include <stdint.h>

uint8_t	ft_strcmp(const char *s1, const char *s2)
{
	uint32_t	i;

	i = 0;
	while (s1[i] == s2[i] && s1[i])
		i++;
	return ((uint8_t)s1[i] - (uint8_t)s2[i]);
}
