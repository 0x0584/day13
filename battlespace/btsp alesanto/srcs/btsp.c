#include <unistd.h>

int	main(void)
{
	int		i;
	char	j;
	char	k;
	int		l;

	l = 0;
	while (++l <= 2)
	{
		i = -1;
		while (++i < 10)
		{
			j = '0';
			while (j - 1 != '9')
			{
				k = 'A' + i;
				write(1, &k, 1);
				write(1, &j, 1);
				write(1, "\n", 1);
				j++;
			}
		}
	}
}
